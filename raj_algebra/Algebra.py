class Algebra:

    def addition(self, a, b):
        """Function to do addition
        
        Arguments:
            a (int) representing 1st number
            b (int) representing 2nd number
            
        Returns: 
            (int) addition of a & b
        """
        return a + b

    def subtraction(self, a, b):
        """Function to do subtraction
        
        Arguments:
            a (int) representing 1st number
            b (int) representing 2nd number
            
        Returns: 
            (int) subtraction of a & b
        """
        return a - b

    def multiplication(self, a, b):
        """Function to do multiplication
        
        Arguments:
            a (int) representing 1st number
            b (int) representing 2nd number
            
        Returns: 
            (int) multiplication of a & b
        """
        return a * b

    def division(self, a, b):
        """Function to do division
        
        Arguments:
            a (int) representing 1st number
            b (int) representing 2nd number
            
        Returns: 
            (int) division of a & b
        """
        return a / b